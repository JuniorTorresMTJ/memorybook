import React, { useState, useEffect } from 'react';
import { useNavigate } from 'react-router-dom';
import { Modal } from './Modal';
import { Heart, Mail, Lock, ArrowRight, Loader2 } from 'lucide-react';
import { Button } from './Button';
import { useAuth } from '../../contexts/AuthContext';
import { useLanguage } from '../../contexts/LanguageContext';

interface AuthModalProps {
    isOpen: boolean;
    onClose: () => void;
}

export const AuthModal: React.FC<AuthModalProps> = ({ isOpen, onClose }) => {
    const [isLogin] = useState(true);
    const [isSubmitting, setIsSubmitting] = useState(false);
    const navigate = useNavigate();
    const { error, clearError } = useAuth();
    const { t } = useLanguage();

    // Clear error when switching between login/register
    useEffect(() => {
        clearError();
    }, [isLogin, clearError]);

    // Clear form when modal closes
    useEffect(() => {
        if (!isOpen) {
            setIsSubmitting(false);
            clearError();
        }
    }, [isOpen, clearError]);

    const handleGuestLogin = () => {
        onClose();
        navigate('/dashboard');
    };

    return (
        <Modal isOpen={isOpen} onClose={onClose}>
            <div className="flex flex-col items-center">
                {/* Icon */}
                <div className="w-16 h-16 bg-primary-teal/10 rounded-full flex items-center justify-center mb-6">
                    <Heart className="w-8 h-8 text-primary-teal fill-current" />
                </div>

                {/* Title */}
                <h2 className="text-3xl font-bold text-text-main mb-2">
                    {isLogin ? t.auth?.welcomeBack || 'Welcome Back' : t.auth?.joinFamily || 'Join the Family'}
                </h2>
                <p className="text-text-muted text-center mb-8">
                    {isLogin
                        ? t.auth?.continuePreserving || 'Continue preserving the moments that matter.'
                        : t.auth?.startPreserving || 'Start preserving precious memories today.'}
                </p>

                {/* Error Message */}
                {error && (
                    <div className="w-full mb-4 p-3 bg-red-50 border border-red-200 rounded-xl text-red-600 text-sm">
                        {error}
                    </div>
                )}

                {/* Main Action - Continue as Guest */}
                <Button
                    type="button"
                    variant="primary"
                    className="w-full !py-4 !text-lg flex items-center justify-center gap-2"
                    onClick={handleGuestLogin}
                    disabled={isSubmitting}
                >
                    {isSubmitting ? (
                        <Loader2 className="w-5 h-5 animate-spin" />
                    ) : (
                        <>
                            <ArrowRight className="w-5 h-5" />
                            {t.auth?.signIn || 'Entrar'}
                        </>
                    )}
                </Button>

                {/* Disabled Login Options - Coming Soon */}
                <div className="space-y-3 opacity-50">
                    <p className="text-xs text-center text-gray-400 uppercase tracking-wider">
                        Em breve
                    </p>
                    
                    {/* Form - Disabled */}
                    <form className="w-full space-y-4 pointer-events-none">
                        <div>
                            <label className="block text-sm font-semibold text-gray-400 mb-2">
                                {t.auth?.email || 'Email'}
                            </label>
                            <div className="relative">
                                <input
                                    type="email"
                                    placeholder={t.auth?.enterEmail || 'nome@exemplo.com'}
                                    className="w-full px-4 py-3 pl-12 border-2 border-gray-200 rounded-2xl bg-gray-50 text-gray-400 placeholder:text-gray-300 cursor-not-allowed"
                                    disabled
                                />
                                <div className="absolute left-4 top-1/2 -translate-y-1/2">
                                    <Mail className="w-5 h-5 text-gray-300" />
                                </div>
                            </div>
                        </div>

                        <div>
                            <label className="block text-sm font-semibold text-gray-400 mb-2">
                                {t.auth?.password || 'Senha'}
                            </label>
                            <div className="relative">
                                <input
                                    type="password"
                                    placeholder={t.auth?.enterPassword || 'Digite sua senha'}
                                    className="w-full px-4 py-3 pl-12 pr-12 border-2 border-gray-200 rounded-2xl bg-gray-50 text-gray-400 placeholder:text-gray-300 cursor-not-allowed"
                                    disabled
                                />
                                <div className="absolute left-4 top-1/2 -translate-y-1/2">
                                    <Lock className="w-5 h-5 text-gray-300" />
                                </div>
                            </div>
                        </div>

                        <button
                            type="button"
                            disabled
                            className="w-full py-3 rounded-2xl bg-gray-100 text-gray-400 font-medium cursor-not-allowed"
                        >
                            {t.auth?.signIn || 'Entrar com Email'}
                        </button>
                    </form>

                    {/* Google Sign In - Disabled */}
                    <button
                        type="button"
                        disabled
                        className="w-full py-3 flex items-center justify-center gap-3 border-2 border-gray-200 rounded-2xl bg-gray-50 text-gray-400 font-medium cursor-not-allowed"
                    >
                        <svg className="w-5 h-5 grayscale" viewBox="0 0 24 24">
                            <path fill="#9CA3AF" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"/>
                            <path fill="#9CA3AF" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"/>
                            <path fill="#9CA3AF" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"/>
                            <path fill="#9CA3AF" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"/>
                        </svg>
                        Google
                    </button>
                </div>

                {/* Info text */}
                <p className="text-xs text-center text-text-muted mt-4">
                    Ao continuar, você concorda com nossos Termos de Uso e Política de Privacidade.
                </p>
            </div>
        </Modal>
    );
};
